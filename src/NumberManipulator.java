public class NumberManipulator {
        public void incrementByOne(){
            int num1 = 1;
            num1++;
            System.out.println("Number after increment is: " + num1);
        }
        public void decrementByOne(){
            int num2 = 2;
            num2--;
            System.out.println("Number after decrement is: " + num2);
        }
}
